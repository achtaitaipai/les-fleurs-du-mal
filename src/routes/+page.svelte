	<h1>À propos</h1>
	<p>
		Le plus souvent, lorsqu'il existe plusieurs versions d'un même texte, c'est sur la dernière
		version que l'on se base. Dans le cas du recueil <em>Les Fleurs du mal</em>, il est plus
		difficile de faire un choix. La censure de la première version oblige Baudelaire à enlever
		certains textes et à rompre l'harmonie qui assemblait les 100 poèmes. Néanmoins, il profite de
		cette réécriture pour ajouter de nouveaux textes, améliorer certains des anciens et modifier
		l'agencement du recueil. Ce site propose un moyen de visualiser les changements qui ont été
		faits entre les deux versions. Vous pouvez naviguer en utilisant le sommaire de 1857 ou de 1861,
		le surlignage rouge signifie que le passage a été supprimé par la suite et le surlignage en vert
		signifie que le passage a été ajouté.
	</p>
